// a[] and b[] are integer arrays.
// Time Complexity: O(n + m).

for (int i = 0; i < n; i++) {
    cout << a[i] << " ";
}
for (int i = 0; i < m; i++) {
    cout << b[i] << " ";
}

// ----------------------------------------------------------

// 's' is a string. Length of the string = m.
// Time complexity: O(n * m)

for (int i = 0; i < n; i++) {
    cout << s << " ";
}

// ----------------------------------------------------------

// Time complexity: O(n * m)

for (int i = 0; i < n; i++) {
    for (int j = 0; j < m; j++) {
        cout << "hello world!";
    }
}

// ----------------------------------------------------------

// Time Complexity: O(n * n).
for (int i = 0; i < n; i++) {
    for (int j = i + 1; j < n; j++) {
        cout << 10 << endl;
    }
}

// ----------------------------------------------------------

// Time Complexity: O(n)
for (int i = 0; i < n; i++) {
    for (int j = n - 2; i < n; i++) {
        cout << 10 << endl;
    }
}

